from itertools import tee


def pairwise(iterable):
    "s -> (s0,s1), (s1,s2), (s2, s3), ..."
    a, b = tee(iterable)
    next(b, None)
    return zip(a, b)


def translate(seq):
    codons = (seq[i:j] for i, j in pairwise(range(0, len(seq) + 1, 3)))
    aa_seq = "".join(codon_table[c] for c in codons)
    # Split sequence at a stop codon ('!')
    return aa_seq[: aa_seq.find("!")]


